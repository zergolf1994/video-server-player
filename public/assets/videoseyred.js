function audioMenu(islem) {
  if (playerContainer.length < 1) playerContainer = vsplayer.getContainer();
  if (islem == "open") {
    $(playerContainer).find(".jw-tooltip").removeClass("jw-open");
    $(playerContainer)
      .find(".jw-controls")
      .addClass("jw-settings-open")
      .attr("v-touch", "true");
    $(playerContainer).find(".jw-settings-menu").attr("aria-expanded", "true");
    $(playerContainer)
      .find(".jw-settings-submenu-audioTracks")
      .addClass("jw-settings-submenu-active")
      .attr("aria-expanded", "true");
    $(playerContainer)
      .find(".jw-settings-topbar .jw-settings-topbar-buttons .jw-icon")
      .attr("aria-expanded", "false");
    $(playerContainer)
      .find(
        ".jw-settings-topbar .jw-settings-topbar-buttons .jw-icon.jw-settings-audioTracks"
      )
      .attr("aria-expanded", "true");
  } else if (islem == "close") {
    $(playerContainer)
      .find(".jw-controls")
      .removeClass("jw-settings-open")
      .removeAttr("v-touch");
    $(playerContainer).find(".jw-settings-menu").attr("aria-expanded", "false");
    $(playerContainer)
      .find(".jw-submenu-audioTracks")
      .attr("aria-expanded", "false");
    $(playerContainer)
      .find(".jw-settings-submenu-audioTracks")
      .removeClass("jw-settings-submenu-active")
      .attr("aria-expanded", "false");
  } else {
    $(playerContainer)
      .find(".jw-submenu-audioTracks")
      .attr("aria-expanded", "false");
    $(playerContainer)
      .find(".jw-settings-submenu-audioTracks")
      .removeClass("jw-settings-submenu-active")
      .attr("aria-expanded", "false");
    $(playerContainer).find(".jw-controls").removeAttr("v-touch");
  }
}
